-- returns the three color components on a string
-- of the form 'R G B'
function Config:GetColorRGB(color)
  local ret = {}
  local w
  local i = 1
  for w in string.gfind(color, '%d+') do
    ret[i] = tonumber(w)
    i = i + 1
  end
  return ret[1], ret[2], ret[3]
