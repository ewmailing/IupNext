-- Receives a date in string form : 'DD/MM/YYYY' 
-- Returns day,month,year as numbers
-- Returns nil if unsuccessful
function Config:GetDate(date)
  local s, e, d, m, y = string.find(date, '(%d%d?)/(%d%d?)/(%d%d%d%d)')
  if not s then
    return
  end
  d = tonumber(d)
  m = tonumber(m)
  y = tonumber(y)
  if not d or not m or not y then
    return
  end
  return d,m,y
end


-- earp: 11out05
function Config:GetDateStr(t)
  local s = ""
  if not t then
    return s
  end
  if t.day then
    if t.day < 10 then
      s = s .. "0"..t.day .. "/"
    else
      s = s .. t.day .. "/"
    end
  end
  if t.month then
    if t.month < 10 then
      s = s .. "0"..t.month .. "/"
    else
      s = s .. t.month .. "/"
    end
  end
  if t.year then
    s = s .. t.year
  end
  return s
end
-- Receives a date in string form : 'DD/MM/YYYY' 
-- Returns it's double representation
-- Returns nil if unsuccessful
function Config:DateToDouble(date)
  local d,m,y = Config:GetDate(date)
  if not d then
    return
  end
  return PSUtil:DateToDouble(d,m,y)
end
-- Receives a date in it's double representation
-- Returns it's string form : 'DD/MM/YYYY'
function Config:DoubleToDate(date)
  local d,m,y = PSUtil:DoubleToDate(date)
  return d..'/'..m..'/'..y
end


