-- returns the directory of the file, filename
function Config:GetDir(file)
  local i = string.find(file, "[\\/]")
  if (i == nil) then
   return './',file
  end


  local match = {char = "[\\/]",place = i}


  while (i ~= nil) do
   match.place = i
   i = string.find(file,match.char, match.place + 1)
  end
 
  local dir = string.sub(file,1,match.place)
  local file = string.sub(file, match.place + 1)
  return dir,file
end


