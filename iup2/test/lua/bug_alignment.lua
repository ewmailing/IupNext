-- Creates the configuration directory tree under the specified directory
-- dir
--  |
--  + config.cfg -- the configuration file for the program
--  |
--  +--- data    -- data directory
--        |
--        +--- macros     -- user macros
--               |
--               +--- lib -- user functions (extensions)
--        |
--        +--- palettes   -- user palettes
--        +--- videos     -- user videos
function Config:MakeDirTree(dir)
  return
    utl.create_directory(dir)                     ~= utl.MKDIR_ERROR_PATH_NOT_FOUND and
    utl.create_directory(dir..'/data')            ~= utl.MKDIR_ERROR_PATH_NOT_FOUND and
    utl.create_directory(dir..'/data/macros')     ~= utl.MKDIR_ERROR_PATH_NOT_FOUND and
    utl.create_directory(dir..'/data/macros/lib') ~= utl.MKDIR_ERROR_PATH_NOT_FOUND and
    utl.create_directory(dir..'/data/palettes')   ~= utl.MKDIR_ERROR_PATH_NOT_FOUND and
    utl.create_directory(dir..'/data/videos')     ~= utl.MKDIR_ERROR_PATH_NOT_FOUND
end



-- Saves a configuration (table) to 'filename', with the given name
-- The name will be used to validate the configuration when loaded
-- Returns true in case of success, false otherwie
function Config:SaveConfig(filename, config, signature)
  f = io.open(filename, 'w')
  if not f then
          print('failed to open', filename)
    return false
  end
  f:write('return {\n')
  dumptable(signature, config)
  f:write('}\n')
  f:close()
  return true
end


-- Loads a configuration (table) from 'filename', with the given name
-- Validates the configuration with the given name
-- Returns (true, table) in case of success, false otherwise
function Config:LoadConfig(filename, signature)
  local ok, ret = pcall(dofile, filename)


  if not ok or not ret or not ret[signature] then
    return false
  else
    return true, ret[signature]
  end
end


-- Clones a configuration table
-- All types of data are copied using lua's '=' operator, except tables which are recursively cloned.
function Config:Clone(config)
