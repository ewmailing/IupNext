-- private methods
function dumptable(name, table, level)
  if not level then
    level = 2
  end


  f:write''
  local i = 1
  while i<=(level-1) do
    f:write('\t')
    i = i + 1
  end


  if type(name) == 'string' then
    f:write('["'..name..'"] = {')
  else
    f:write('['..name..'] = {')
  end


  if type(table) == 'array' then
    f:write' '
    for i,v in ipairs(table) do
      f:write(v..',')
    end
    f:write('},\n')
    return
  end
 
  f:write'\n'


  local v
  for i,v in pairs(table) do
    if type(v) == 'table' then
      dumptable(i, v, level + 1)
    else
      local l = 1
      while l<=level do
        f:write('\t')
        l = l + 1
      end
  
      if type(i) == 'string' then
        f:write('["'..i..'"] = ')
      else
        f:write('['..i..'] = ')
      end
  
      if type(v) == 'string' then
        f:write('[['..v..']]')
      elseif type(v) == 'userdata' then
        f:write('"USERDATA"')
      elseif type(v) == 'boolean' then
        f:write(tostring(v))
      else
        f:write(v)
      end
      f:write',\n'
    end
  end
 
  i = 1
  while i<=(level-1) do
    f:write('\t')
    i = i + 1
  end
  f:write('},\n')
end


function breakpath(path)
  local i = string.find(path, "[\\/]")
  if (i == nil) then
   return './',path
  end


  local match = {char = "[\\/]",place = i}


  while (i ~= nil) do
   match.place = i
   i = string.find(path,match.char, match.place + 1)
  end
 
  local dir = string.sub(path,1,match.place)
  local file = string.sub(path, match.place + 1)
  return dir,file
end


-- clones a table
-- copies by value all types of data except tables, which are recursively cloned
function clonetable(t)
  local ret = {}
  for k,v in pairs(t) do
    if type(v) ~= 'table' then
      ret[k] = v
    else
      ret[k] = clonetable(v)
    end
  end
  return ret
end



